//Comparision Operators

//Greater Than
console.log(100 > 20);

//Less Than
console.log(90 < 89);

//is Equal to
console.log(10 == (2 * 5));

// Greater Than or Equal to
console.log(9 >= 10);

//Less than or Equal to
console.log(100 <= 99);


//Not Equal to
console.log(10 != 90);

//Strictly Equal to
console.log(10 === "10");
console.log(10 == "10");


//Not Strictly Equal to
console.log(10 !== "10");
console.log(10 != "10");


console.log("ABC" == "abc");